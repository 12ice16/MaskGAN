class SPP(nn.Module):
    # Spatial Pyramid Pooling (SPP) layer https://arxiv.org/abs/1406.4729
    '''
        c_ = c1 // 2  # hidden channels

        --> Conv(c1,c_,k1,s1) --> maxpool(k5,s1,p=5/2) -- |
                                | maxpool(k9,s1,p=9/2) -- |
                                | maxpool(k13,s1,p=13/2) -- |
                                | --------------------------> cat --> Conv(c_ * (len(k) + 1), c2,k1,s1)
    '''
    def __init__(self, c1, c2, k=(5, 9, 13)):
        super().__init__()
        c_ = c1 // 2  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(c_ * (len(k) + 1), c2, 1, 1)
        self.m = nn.ModuleList([nn.MaxPool2d(kernel_size=x, stride=1, padding=x // 2) for x in k])

    def forward(self, x):
        x = self.cv1(x)
        with warnings.catch_warnings():
            warnings.simplefilter('ignore')  # suppress torch 1.9.0 max_pool2d() warning
            return self.cv2(torch.cat([x] + [m(x) for m in self.m], 1))


class SPPF(nn.Module):
    # Spatial Pyramid Pooling - Fast (SPPF) layer for YOLOv5 by Glenn Jocher
    '''
        input_size:(1, c1, h, w)   --->   output_size:(1, c2, h, w)
    '''
    def __init__(self, c1, c2, k=5, bn='batch'):  # equivalent to SPP(k=(5, 9, 13))
        super().__init__()
        c_ = c1 // 2  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1,bn=bn)
        self.cv2 = Conv(c_ * 4, c2, 1, 1,bn=bn)
        self.m = nn.MaxPool2d(kernel_size=k, stride=1, padding=k // 2)

    def forward(self, x):
        x = self.cv1(x)
        with warnings.catch_warnings():
            warnings.simplefilter('ignore')  # suppress torch 1.9.0 max_pool2d() warning
            y1 = self.m(x)
            y2 = self.m(y1)
            return self.cv2(torch.cat((x, y1, y2, self.m(y2)), 1))

class FP(nn.Module):
    # fuse pooling
    '''
        x=torch.cat(maxpool,avgpool),缩小r倍,（b,c1,h1,w1）   -->   （b,2*c1,h1/r,w1/r）
    '''
    def __init__(self,c1,c2,r=4):
        '''
        r: 缩放倍数，为2的整数次方
        '''
        super().__init__()
        self.m = nn.MaxPool2d(kernel_size=r, stride=r)
        self.a = nn.AvgPool2d(kernel_size=r, stride=r)
        self.cv1=Conv(2*c1,c2)

    def forward(self, x):
        with warnings.catch_warnings():
            warnings.simplefilter('ignore')  # suppress torch 1.9.0 max_pool2d() warning
            bs,c1,_,_=x.shape
            y1 = self.m(x)
            y2 = self.a(x)
            return self.cv1(torch.cat(( y1, y2), 1))


class Focus(nn.Module):
    # Focus wh information into c-space
    '''
         input_size:(1, c1, h, w)   --->   output_size:(1, c2, h/2, w/2)
    '''
    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.conv = Conv(c1 * 4, c2, k, s, p, g, act=act)
        # self.contract = Contract(gain=2)

    def forward(self, x):  # x(b,c,w,h) -> y(b,4c,w/2,h/2)
        return self.conv(torch.cat((x[..., ::2, ::2], x[..., 1::2, ::2], x[..., ::2, 1::2], x[..., 1::2, 1::2]), 1))
