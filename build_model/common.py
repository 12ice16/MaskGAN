class UpConv(nn.Module):
    # Standard convolution with args(ch_in, ch_out, kernel, stride, padding, groups, dilation, activation)
    default_act = nn.SiLU()  # default activation

    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, d=1, act=True):
        super().__init__()
        self.upconv = nn.ConvTranspose2d(c1, c2, k, s, autopad(k, p, d), groups=g, dilation=d, bias=False)
        self.bn = nn.BatchNorm2d(c2)
        self.act = self.default_act if act is True else act if isinstance(act, nn.Module) else nn.Identity()

    def forward(self, x):
        # return self.act(self.bn(self.upconv(x)))
        return self.bn(self.upconv(self.act(x)))

    def forward_fuse(self, x):
        # return self.act(self.upconv(x))
        return self.upconv(self.act(x))



class DWConv(Conv):
    # Depth-wise convolution
    '''
        本质就是将conv卷积的groups=g=1,自动改为g=math.gcd(c1, c2),  即c1和c2的最大公约数。
        g=math.gcd(c1, c2) 表明 groups 参数设置为输入通道数 c1 和输出通道数 c2 的最大公约数。
        在深度可分离卷积中，一般情况下会将 groups 设为输入通道数 c1（因为每个输入通道都有一个单独的卷积核），
        但这里使用最大公约数可能是为了适应某些特殊情况或者简化实现。
        实际上，在标准深度可分离卷积中，通常 groups=c1 并且 c1 == c2，确保每个输入通道各自对应一个深度卷积核。
    '''
    def __init__(self, c1, c2, k=1, s=1, d=1, act=True,bn='batch'):  # ch_in, ch_out, kernel, stride, dilation, activation
        super().__init__(c1, c2, k, s, g=math.gcd(c1, c2), d=d, act=act,bn=bn)
        '''
            math.gcd 是 Python 标准库 math 模块中提供的一个函数，全称为 Greatest Common Divisor，中文译为“最大公约数”。
            这个函数用于计算两个或更多整数之间的最大公约数（GCD），也就是这些数所能整除的最大正整数。
            从 Python 3.5 开始，math.gcd 函数可以直接接受两个或更多的参数，返回所有参数的最大公约数。
        '''


